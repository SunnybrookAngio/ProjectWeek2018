# object which saves a volumeNode and a threshold
class SlicerVMTKLevelSetContainer(object):

    def __init__(self,node,threshold):

        self._node = node
        self._threshold = threshold


    def GetNode(self):

        return self._node

    def GetThreshold(self):

        return self._threshold

    def SetNode(self,newnode):
        self._node = newnode

    def SetThreshold(self,newthreshold):
        self._threshold = newthreshold
